    process_date,
    client_id, custodian, account_no,
    sum(usde_market_value) AS usde_market_value
FROM public.vw_reporte_maestro_datos_saldos
GROUP BY process_date, client_id, custodian, account_no
HAVING process_date = '20250605'
;

